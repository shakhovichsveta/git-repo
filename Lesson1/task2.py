# Выполнить логические побитовые операции "И","ИЛИ" и др. над числами 5 и 6.
# Выполнить над числом 5 побитовый сдвиг вправо или влево на 2 знака
a = 5
print(f'{a} = {bin(a)}')

b = 6
print(f'{b} = {bin(b)}')
print(f'{a} & {b} = {bin(a&b)}')
print(f'{a} | {b} = {bin(a|b)}')
print(f'{a} ^ {b} = {bin(a^b)}')
print(f'{a} << 2 = {bin(a<<2)}')
print(f'{a} >> 2 = {bin(a>>2)}')
